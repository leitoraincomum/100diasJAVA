public class Exercicio09 {
    public static void main(String[] args) {
        //9. Faça um programa que imprima na tela apenas os números ímpares entre 1 e 50.
        System.out.println("** Exibir impares de 1 a 50 **");
        for (int i = 1; i < 50; i++){
            if (i % 2 != 0){
                System.out.print(i + " ");
            }
        }
    }   
}//revisado 31/10/2023
